/*
 * Get a encryption passphrase.
 */
#[no_mangle]
pub unsafe extern "C" fn __archive_write_get_passphrase(
    mut a: *mut archive_write,
) -> *const libc::c_char {
    if !(*a).passphrase.is_null() {
        return (*a).passphrase;
    }
    if (*a).passphrase_callback.is_some() {
        let mut p: *const libc::c_char = 0 as *const libc::c_char;
        p = (*a).passphrase_callback.expect("non-null function pointer")(
            &mut (*a).archive,
            (*a).passphrase_client_data,
        );
        if !p.is_null() {
            (*a).passphrase = strdup(p);
            if (*a).passphrase.is_null() {
                archive_set_error(
                    &mut (*a).archive as *mut archive,
                    ENOMEM,
                    b"Can\'t allocate data for passphrase\x00" as *const u8 as *const libc::c_char,
                );
                return 0 as *const libc::c_char;
            }
            return (*a).passphrase;
        }
    }
    return 0 as *const libc::c_char;
}
