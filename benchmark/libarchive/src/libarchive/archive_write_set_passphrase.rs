/*
 * Set a encryption passphrase.
 */
#[no_mangle]
pub unsafe extern "C" fn archive_write_set_passphrase(
    mut _a: *mut archive,
    mut p: *const libc::c_char,
) -> libc::c_int {
    let mut a: *mut archive_write = _a as *mut archive_write;
    let mut magic_test: libc::c_int = __archive_check_magic(
        _a,
        0xb0c5c0de as libc::c_uint,
        1 as libc::c_uint,
        b"archive_write_set_passphrase\x00" as *const u8 as *const libc::c_char,
    );
    if magic_test == ARCHIVE_FATAL {
        return ARCHIVE_FATAL;
    }
    if p.is_null() || *p.offset(0 as libc::c_int as isize) as libc::c_int == '\u{0}' as i32 {
        archive_set_error(
            &mut (*a).archive as *mut archive,
            ARCHIVE_ERRNO_MISC,
            b"Empty passphrase is unacceptable\x00" as *const u8 as *const libc::c_char,
        );
        return -(25 as libc::c_int);
    }
    free((*a).passphrase as *mut libc::c_void);
    (*a).passphrase = strdup(p);
    if (*a).passphrase.is_null() {
        archive_set_error(
            &mut (*a).archive as *mut archive,
            ENOMEM,
            b"Can\'t allocate data for passphrase\x00" as *const u8 as *const libc::c_char,
        );
        return -(30 as libc::c_int);
    }
    return 0 as libc::c_int;
}
#[no_mangle]
pub unsafe extern "C" fn archive_write_set_passphrase_callback(
    mut _a: *mut archive,
    mut client_data: *mut libc::c_void,
    mut cb: Option<archive_passphrase_callback>,
) -> libc::c_int {
    let mut a: *mut archive_write = _a as *mut archive_write;
    let mut magic_test: libc::c_int = __archive_check_magic(
        _a,
        0xb0c5c0de as libc::c_uint,
        1 as libc::c_uint,
        b"archive_write_set_passphrase_callback\x00" as *const u8 as *const libc::c_char,
    );
    if magic_test == ARCHIVE_FATAL {
        return ARCHIVE_FATAL;
    }
    (*a).passphrase_callback = cb;
    (*a).passphrase_client_data = client_data;
    return 0 as libc::c_int;
}
