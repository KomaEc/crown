/* *
 * section: Tree
 * synopsis: Navigates a tree to print element names
 * purpose: Parse a file to a tree, use xmlDocGetRootElement() to
 *          get the root element, then walk the document and print
 *          all the element name in document order.
 * usage: tree1 filename_or_URL
 * test: tree1 test2.xml > tree1.tmp && diff tree1.tmp $(srcdir)/tree1.res
 * author: Dodji Seketeli
 * copy: see Copyright for the status of this software.
 */
/*
 *To compile this file using gcc you can type
 *gcc `xml2-config --cflags --libs` -o xmlexample libxml2-example.c
 */
/* *
 * print_element_names:
 * @a_node: the initial xml node to consider.
 *
 * Prints the names of the all the xml elements
 * that are siblings or children of a given xml node.
 */
unsafe extern "C" fn print_element_names(mut a_node: *mut xmlNode) {
    let mut cur_node: *mut xmlNode = 0 as *mut xmlNode;
    cur_node = a_node;
    while !cur_node.is_null() {
        if (*cur_node).type_0 as std::os::raw::c_uint ==
               XML_ELEMENT_NODE as std::os::raw::c_int as std::os::raw::c_uint {
            printf(b"node type: Element, name: %s\n\x00" as *const u8 as
                       *const std::os::raw::c_char, (*cur_node).name);
        }
        print_element_names((*cur_node).children);
        cur_node = (*cur_node).next
    };
}
/* *
 * Simple example to parse a file called "file.xml", 
 * walk down the DOM, and print the name of the 
 * xml elements nodes.
 */
unsafe fn main_0(mut argc: std::os::raw::c_int, mut argv: *mut *mut std::os::raw::c_char)
 -> std::os::raw::c_int {
    let mut doc: *mut xmlDoc = 0 as *mut xmlDoc;
    let mut root_element: *mut xmlNode = 0 as *mut xmlNode;
    if argc != 2 as std::os::raw::c_int { return 1 as std::os::raw::c_int }
    /*
     * this initialize the library and check potential ABI mismatches
     * between the version it was compiled for and the actual shared
     * library used.
     */
    xmlCheckVersion(20908 as std::os::raw::c_int);
    /*parse the file and get the DOM */
    doc =
        xmlReadFile(*argv.offset(1 as std::os::raw::c_int as isize),
                    0 as *const std::os::raw::c_char, 0 as std::os::raw::c_int);
    if doc.is_null() {
        printf(b"error: could not parse file %s\n\x00" as *const u8 as
                   *const std::os::raw::c_char,
               *argv.offset(1 as std::os::raw::c_int as isize));
    }
    /*Get the root element node */
    root_element = xmlDocGetRootElement(doc);
    print_element_names(root_element);
    /*free the document */
    xmlFreeDoc(doc);
    /*
     *Free the global variables that may
     *have been allocated by the parser.
     */
    xmlCleanupParser();
    return 0 as std::os::raw::c_int;
}
#[main]
pub fn main() {
    let mut args: Vec<*mut std::os::raw::c_char> = Vec::new();
    for arg in ::std::env::args() {
        args.push(::std::ffi::CString::new(arg).expect("Failed to convert argument into CString.").into_raw());
    };
    args.push(::std::ptr::null_mut());
    unsafe {
        ::std::process::exit(main_0((args.len() - 1) as std::os::raw::c_int,
                                    args.as_mut_ptr() as
                                        *mut *mut std::os::raw::c_char) as i32)
    }
}
