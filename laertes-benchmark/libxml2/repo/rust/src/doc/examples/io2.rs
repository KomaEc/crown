/*
 * The XML memory wrapper support 4 basic overloadable functions.
 */
/* *
 * xmlFreeFunc:
 * @mem: an already allocated block of memory
 *
 * Signature for a free() implementation.
 */
pub type xmlFreeFunc
    =
    Option<unsafe extern "C" fn(_: *mut std::os::raw::c_void) -> ()>;
/* *
 * section: InputOutput
 * synopsis: Output to char buffer
 * purpose: Demonstrate the use of xmlDocDumpMemory
 *          to output document to a character buffer
 * usage: io2
 * test: io2 > io2.tmp && diff io2.tmp $(srcdir)/io2.res
 * author: John Fleck
 * copy: see Copyright for the status of this software.
 */
unsafe fn main_0() -> std::os::raw::c_int {
    let mut n: xmlNodePtr = 0 as *mut xmlNode;
    let mut doc: xmlDocPtr = 0 as *mut xmlDoc;
    let mut xmlbuff: *mut xmlChar = 0 as *mut xmlChar;
    let mut buffersize: std::os::raw::c_int = 0;
    /*
     * Create the document.
     */
    doc =
        xmlNewDoc(b"1.0\x00" as *const u8 as *const std::os::raw::c_char as
                      *mut xmlChar);
    n =
        xmlNewNode(0 as xmlNsPtr,
                   b"root\x00" as *const u8 as *const std::os::raw::c_char as
                       *mut xmlChar);
    xmlNodeSetContent(n,
                      b"content\x00" as *const u8 as *const std::os::raw::c_char as
                          *mut xmlChar);
    xmlDocSetRootElement(doc, n);
    /*
     * Dump the document to a buffer and print it
     * for demonstration purposes.
     */
    xmlDocDumpFormatMemory(doc, &mut xmlbuff, &mut buffersize,
                           1 as std::os::raw::c_int);
    printf(b"%s\x00" as *const u8 as *const std::os::raw::c_char,
           xmlbuff as *mut std::os::raw::c_char);
    /*
     * Free associated memory.
     */
    xmlFree.expect("non-null function pointer")(xmlbuff as *mut std::os::raw::c_void);
    xmlFreeDoc(doc);
    return 0 as std::os::raw::c_int;
}
#[main]
pub fn main() { unsafe { ::std::process::exit(main_0() as i32) } }
