/* *
 * section: Parsing
 * synopsis: Parse an XML file to a tree and free it
 * purpose: Demonstrate the use of xmlReadFile() to read an XML file
 *          into a tree and and xmlFreeDoc() to free the resulting tree
 * usage: parse1 test1.xml
 * test: parse1 test1.xml
 * author: Daniel Veillard
 * copy: see Copyright for the status of this software.
 */
/* *
 * example1Func:
 * @filename: a filename or an URL
 *
 * Parse the resource and free the resulting tree
 */
unsafe extern "C" fn example1Func(mut filename: *const std::os::raw::c_char) {
    let mut doc: xmlDocPtr =
        0 as *mut xmlDoc; /* the resulting document tree */
    doc = xmlReadFile(filename, 0 as *const std::os::raw::c_char, 0 as std::os::raw::c_int);
    if doc.is_null() {
        fprintf(stderr,
                b"Failed to parse %s\n\x00" as *const u8 as
                    *const std::os::raw::c_char, filename);
        return
    }
    xmlFreeDoc(doc);
}
unsafe fn main_0(mut argc: std::os::raw::c_int, mut argv: *mut *mut std::os::raw::c_char)
 -> std::os::raw::c_int {
    if argc != 2 as std::os::raw::c_int { return 1 as std::os::raw::c_int }
    /*
     * this initialize the library and check potential ABI mismatches
     * between the version it was compiled for and the actual shared
     * library used.
     */
    xmlCheckVersion(20908 as std::os::raw::c_int);
    example1Func(*argv.offset(1 as std::os::raw::c_int as isize));
    /*
     * Cleanup function for the XML library.
     */
    xmlCleanupParser();
    /*
     * this is to debug memory for regression tests
     */
    xmlMemoryDump();
    return 0 as std::os::raw::c_int;
}
#[main]
pub fn main() {
    let mut args: Vec<*mut std::os::raw::c_char> = Vec::new();
    for arg in ::std::env::args() {
        args.push(::std::ffi::CString::new(arg).expect("Failed to convert argument into CString.").into_raw());
    };
    args.push(::std::ptr::null_mut());
    unsafe {
        ::std::process::exit(main_0((args.len() - 1) as std::os::raw::c_int,
                                    args.as_mut_ptr() as
                                        *mut *mut std::os::raw::c_char) as i32)
    }
}
